#include <stdio.h>
/**
* main - entry point
* @void: return no value if true
*
* Return: always 0
*
* Alphabets in lower and UPPER caSE
*/
	int main(void)
	{
	int ch;
	{
	for (ch = 'a'; ch <= 'z'; ch++)
		putchar(ch);
	for (ch = 'A'; ch <= 'Z'; ch++)
		putchar(ch);
	}
		putchar('\n');
	return (0);
}
